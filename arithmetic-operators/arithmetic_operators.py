#Suma y resta (+ y -)
suma = 12+5
resta = 12-5

print(suma)
print(resta)

#Multiplicación y división (* y /)
multiplicacion = 10*5
division = 12/5 #devuelve un dato flotante o float
print(multiplicacion)
print(division)

#Potenciación exponente (**)
exponente = 12 ** 5
print(exponente)

#División baja (//), devuelve entero redondeado hacia abajo 
division_baja = 12 // 5 
print(division_baja)

#Resto o modulo
resto = 12 % 5
print(resto)